export function getConferencesByCategory(category: ConferenceInfo["category"]) {
  return CONFERENCES.filter((c) => c.category === category);
}

export function getConferenceById(id: string) {
  return CONFERENCES.find((c) => c.id === id.toLowerCase());
}

